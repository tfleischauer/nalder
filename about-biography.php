      <h2>Nan Nalder Vitae</h2>
      
      <p>Born at Grand Coulee Dam in 1938, Nan Nalder grew up at the Dam with a brief sojourn in her youth to Washington, D.C., where Nan discovered ART!  Visits to the National Gallery beginning in 1948 provided Nan with an early taste of the wonders of painting and drawing.  When the family returned to the Dam in the early 1950’s, Nan was privileged to have private art lessons from Leona Clevinger who had received early training at the Chicago Art Institute.  Nan maintained contact with her until Leona’s death.  Another major influence was her Aunt Lila Roche, her father’s eldest sister, who had been professionally trained in Art at Washington State University, the Chicago Art Institute, and the Sorbonne in Paris.  Lila would come to visit the Dam and Nan would accompany her on painting excursions to capture the beauty of the light on the hills surrounding the Dam and the Coulee.</p>

	  <h3>Art School &amp; Graphic Design</h3>
      <p>Nan commenced full-time art studies in 1957 at Washington State University where she studied with Keith Monaghan, Robert Sterling, and Gaylen Hansen.  Nan worked as an assistant in Art History courses and also was very active in Delta Phi Delta, an Arts Honorary.  Her first full-time position upon graduation was as an illustrator for the WSU Press.  
      
      <h3>Architecture</h3>
      <p>Nan continued to work as a graphic designer in Seattle and Medford, Oregon. She became interested in how art is reflected in the built environment beginning with assignments in Medford, Oregon, and then joined a Seattle architecture firm as an Associate in 1968.  She continued to work in architecture in San Francisco, Boston, and Santa Fe designing schools and hospitals and working in integration of solar and wind energy systems in Northern New Mexico.</p>
      
      <h3>The Natural Environment</h3>
      <p>In 1976 she became concerned with the natural environment and how man’s activities were affecting the ecosystems.  Her love of nature guided her art for years and she wanted to better understand some of the underlying forces resulting in major changes in the landscape.  She returned to graduate school in Seattle and worked for many years for federal agencies and engineering firms, continuing to learn why images were changing due to man’s influence.</p> 
      
      <h3>Life and Beauty</h3>
      <p>Nan has drawn and painted all of her life through times of relative calm as well as chaos in her personal and professional life. Her work ranges from the abstract to sensual representations of flowers in her garden. She creates jewelry and is currently working on a series of clerical stoles. Nan is known to often state: “ART is the one thing that keeps me sane and provides an escape from chaos.”  She has exhibited professionally beginning in 1961 and will continue, in her words:  “As long as God allows me this wonderful means to express life and beauty.”
</p>

