      <h3>The Power of Strokes</h3>
      <p>I regard my paintings as an inevitable consequence of growth – outposts marking a progression in development that records over time my development by degrees.  Often they are not ends unto themselves, but indicators of a course taken and progress made.</p>
      
      <p>I revel in the power of strokes – the slippery character of the selected medium – the texture of the surface upon which I place my mark.  The landscape, garden, and each flower present an ever-changing medium for ideas.  I greet nature with a mind open to impressions ever conscious of change in light and texture – and I meet the challenge by recording the beauty witnessed.  I find nature "as is" a very wonderful romance.</p>
      
