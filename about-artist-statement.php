      <h2>Artist's Statement</h2>
      
      <p>The role of art in my life is best captured in a statement made by <a href="http://en.wikipedia.org/wiki/Herbert_Read">Sir Herbert Read</a>: "Art is an escape from chaos!"</p>

      <p>Through painting, I reflect upon that which I find interesting and towards which I am capable of intense feelings and profound contemplation.  I find drawing and painting provide me with an opportunity to reflect on issues of concern to me – from a tumultuous past to a hoped for calmer present. I become totally absorbed in another world and the sense of time disappears.</p> 
      
      <h3>The Act of Painting</h3>
      <p>The act of painting provides me with a sense of place from which I see beyond the realities of the surface and go seeking for that which I love in nature and capture it.  The process is one of ever learning to see anew and understanding through this precious gift of sight an expanded range of images presented by nature; a special and particular vision making itself known through my eyes in which both the seen and the unseen are recombined in a representative whole.</p>
      
      <p>For me the act of painting provides an opportunity to study how ordinary things are transformed by light shining through them; and, to reflect on the relative value of things and knowing that relative value I can do anything.  I can build upon them – I can set aside those that I deem important at that moment and select those that intrigue me.  In nature the most vital elements and activities endure only for a moment – it is the gesture of that moment that recalls the memory of the vitality that I set out to capture.</p>
      
