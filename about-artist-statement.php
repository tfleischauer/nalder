      <h3>Subjects</h3>
       <p>I explore color, form, and line concentrate on three discrete, yet related subjects:</p>
      
        <ul>     
            <li>Landscape, garden, flowers – particularly as light is absorbed and reflected</li>
        
            <li>Collage – use of texture and images to create a new whole</li>
        
            <li>Pattern, motif – often as presented in flat woven rugs and textiles of the near and middle east</li>
        </ul>

	 <h3>Influences</h3>
       <p>Influences on the physical and intellectual aspects of the process of making Art as I have experienced include:</p> 
            
        <ul>
          <li>Monet – light and color and an understanding of the relationships and tensions established between surfaces that absorb light and those that reflect light – and a wonderful day painting in his gardens at Giverney</li>
      
          <li>Bonnard – color and a gentle reflection on gardens, people, and animals</li>
      
          <li>Cezanne – realization that in addition to pure vision, there is a strong need to "realize the sensations."  Composition – to see although it (the subject) may disappear</li>
      
          <li>Delacroix – his journal has provided many hours of peaceful contemplation and has oft times provided the "aha" moment</li>
      
          <li>Matisse – flat pattern and color – the importance of major motif in a complex composition</li>
      
          <li>Klee – playful composition</li>
      
          <li>DeKooning, Joan Mitchell and Helen Frankenthaler – form, color, gesture.  I have just finished reading a biography on DeKooning and again visited prints of his work to enjoy again the vitality he brought to art.</li>
      
          <li>Rauschenberg – composition and a true sense of fun.  I have often created collages using rubbings and pasting and know that Rauschenberg influenced some of what I create.</li>
      
          <li>Rothko – color relationships</li>
        </ul>
      
      	<h3>Recording Impressions</h3>
          <p>For as long as I can remember, I have recorded my impressions of the world as I see it with line, color, sometimes words, but always color.  Certain forms have left indelible markings in my minds eye – some I have captured, some I have left to others.  I tend to connect events, people, observations and just plain gut feelings together in line and color.  Often I develop a series of motifs that become my own personal shorthand of recollections from the seen world.</p>
      
          <p>I cannot remember a day in my life, even the worst ones, when color and line and motif failed to occur in my thought process.  I know musicians who constantly have a tune, or a structure that may become a tune, and they have to rat-a-tat-tat or thump or hum it out. My thought process is similar, but I can paint it in my mind not making a sound.  I wish at times that I could record the ideas as a camera might – but then something happens and it is gone.</p>
      
      <h3>Art Everyday</h3>
      <p>I believe that Art is of the everyday and must be enjoyed everyday.  I react strongly (some say radically) to an atmosphere or setting that I believe is devoid of any resemblance of Art.  I’m not talking about "beauty" – I am talking about integrity.</p>
      
      <p>I work in every media that I can access and often use a "jumble" in creating my work.  I find that each media provides me with yet another way to put on paper or canvas an expression/impression of my visual experiences.</p>
      
      <h3>Liberation</h3>
      <p>Just contemplating the act of painting provides me with a sense of liberation – a release.  I cannot emphasize enough the role that that ability plays in my own sanity. Even when stuck in a dull and boring meeting, I can contemplate a work in my minds eye – my own or another’s and there is a sense of relief that knows no bounds.</p>
      
      <p>As I stated earlier, "Art is an escape from chaos!" (Herbert Read).</p>
      
