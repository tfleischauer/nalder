      <h3>Monet's Gardens</h3> 
      
      <p>I am deeply drawn to gardens and the indescribable beauty through the seasons – my own and places I have been privileged to visit.  A major influence in my recent work is the trip we took to <a href="http://fondation-monet.com/fr/">Monet’s gardens</a> in Giverny in 2001.  I spent a blessed day painting in the water gardens – it was a Monday in June when the gardens are closed to visitors and artists are invited in to paint.</p>  
      
      <p>I found a spot on the pond and spent all day Monday capturing the play of light on the trees, the water, and the water garden.  It was a bit of a sad day – Monet’s favorite willow had fallen into the pond and the gardeners were busy removing it.  We had just seen the tree on Sunday and it was such a surprise to find it "gone."  A lovely woman from the Monet Foundation came around to visit the artists and she explained to me the significance.  I am currently working on a series based on my paintings that day.</p>
      
