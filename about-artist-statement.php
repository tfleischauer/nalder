      <h3>Fish As A Theme</h3>
      <p>Another recurrent image is the fish: fish as a symbol of creation; and fish as a wonderful creature.  I spent many wonderful hours with my Dad fishing in my youth – I tied flies and made my own rod.  I work with restoration of fisheries in my professional life.</p>
      
      <h3>Drawing</h3>
      <p>Drawing is an essential element – line, gesture, mass, space, and the relationships between the light and the dark provide the framework for color.  I begin each painting by drawing the major elements of the composition using pencil or crayon and then proceeding to my brushes.</p>
      
